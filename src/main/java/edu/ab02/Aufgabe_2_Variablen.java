package edu.ab02;

public class Aufgabe_2_Variablen {

    public static void main(String[] args){

        //Variables getting defined and initiated
        int students = 30;
        boolean lightOn = true;
        double account = 1500.25;
        char row = 'D';

        //Variables getting other value and getting print
        students = 35;
        lightOn = false;
        account = 11254.65;
        row = 'E';


        System.out.println(students);
        System.out.println(lightOn);
        System.out.println(account);
        System.out.println(row);

    }
}
